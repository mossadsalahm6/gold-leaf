// Language management
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'ar';
        this.init();
    }
    
    init() {
        this.createLanguageToggle();
        this.applyLanguage(this.currentLanguage);
        this.bindEvents();
    }
    
    createLanguageToggle() {
        // Add language toggle to navigation
        const navbar = document.querySelector('.nav-menu');
        if (navbar) {
            const languageItem = document.createElement('li');
            languageItem.innerHTML = `
                <div class="language-toggle">
                    <button class="language-btn" id="languageToggle">
                        <i class="fas fa-globe"></i>
                        <span class="current-lang">${this.currentLanguage === 'ar' ? 'العربية' : 'English'}</span>
                        <i class="fas fa-chevron-down"></i>
                    </button>
                    <div class="language-dropdown" id="languageDropdown">
                        <a href="#" data-lang="ar" class="language-option ${this.currentLanguage === 'ar' ? 'active' : ''}">
                            العربية
                        </a>
                        <a href="#" data-lang="en" class="language-option ${this.currentLanguage === 'en' ? 'active' : ''}">
                            English
                        </a>
                    </div>
                </div>
            `;
            navbar.appendChild(languageItem);
        }
    }
    
    bindEvents() {
        // Language toggle button
        const toggleBtn = document.getElementById('languageToggle');
        const dropdown = document.getElementById('languageDropdown');
        
        if (toggleBtn && dropdown) {
            toggleBtn.addEventListener('click', (e) => {
                e.preventDefault();
                dropdown.classList.toggle('show');
            });
            
            // Language options
            const options = dropdown.querySelectorAll('.language-option');
            options.forEach(option => {
                option.addEventListener('click', (e) => {
                    e.preventDefault();
                    const lang = option.getAttribute('data-lang');
                    this.changeLanguage(lang);
                    dropdown.classList.remove('show');
                });
            });
            
            // Close dropdown when clicking outside
            document.addEventListener('click', (e) => {
                if (!toggleBtn.contains(e.target) && !dropdown.contains(e.target)) {
                    dropdown.classList.remove('show');
                }
            });
        }
    }
    
    changeLanguage(lang) {
        if (lang !== this.currentLanguage) {
            this.currentLanguage = lang;
            localStorage.setItem('language', lang);
            this.applyLanguage(lang);
            this.updateLanguageToggle();
            
            // Update page direction
            document.documentElement.setAttribute('lang', lang);
            document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
            
            // Trigger custom event for other components
            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
        }
    }
    
    updateLanguageToggle() {
        const currentLangSpan = document.querySelector('.current-lang');
        const options = document.querySelectorAll('.language-option');
        
        if (currentLangSpan) {
            currentLangSpan.textContent = this.currentLanguage === 'ar' ? 'العربية' : 'English';
        }
        
        options.forEach(option => {
            const lang = option.getAttribute('data-lang');
            option.classList.toggle('active', lang === this.currentLanguage);
        });
    }
    
    applyLanguage(lang) {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                if (element.tagName === 'INPUT' && element.type === 'submit') {
                    element.value = translations[lang][key];
                } else if (element.hasAttribute('placeholder')) {
                    element.placeholder = translations[lang][key];
                } else {
                    element.textContent = translations[lang][key];
                }
            }
        });
        
        // Update document title
        if (translations[lang] && translations[lang].heroTitle) {
            document.title = `${translations[lang].heroTitle} - ${translations[lang].heroSubtitle}`;
        }
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
    
    translate(key) {
        return translations[this.currentLanguage] && translations[this.currentLanguage][key] 
            ? translations[this.currentLanguage][key] 
            : key;
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    window.languageManager = new LanguageManager();
});

// Export for use in other files
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, LanguageManager };
}

